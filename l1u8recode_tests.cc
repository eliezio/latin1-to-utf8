TEST(L1U8Tests, twoSections) {
    static const uint8_t input[] = {
        0x63, 0x6f, 0x72, 0x61, 0xe7, 0xe3, 0x6f, 0x0d, 0x0a, 0x42, 0x45, 0x47, 0x49, 0x4e, 0x0d, 0x0a,  //coração..BEGIN..
        0x63, 0x6f, 0x72, 0x61, 0xe7, 0xe3, 0x6f, 0x0d, 0x0a, 0x45, 0x4e, 0x44, 0x0d, 0x0a, 0x64, 0x27,  //coração..END..d'
        0xea, 0x74, 0x72, 0x65, 0x0d, 0x0a, 0x42, 0x45, 0x47, 0x49, 0x4e, 0x0d, 0x0a, 0x72, 0xe9, 0x66,  //être..BEGIN..réf
        0x6c, 0xe9, 0x63, 0x68, 0x69, 0x72, 0x0d, 0x0a, 0x45, 0x4e, 0x44, 0x0d, 0x0a, 0x66, 0x75, 0x6d,  //léchir..END..fum
        0xe9, 0x65, 0x0d, 0x0a,                                                                          //ée..
    };
    static const uint8_t expectedOutput[] = {
        0x63, 0x6f, 0x72, 0x61, 0xe7, 0xe3, 0x6f, 0x0d, 0x0a, 0x42, 0x45, 0x47, 0x49, 0x4e, 0x0d, 0x0a,  //coração..BEGIN..
        0x63, 0x6f, 0x72, 0x61, 0xc3, 0xa7, 0xc3, 0xa3, 0x6f, 0x0d, 0x0a, 0x45, 0x4e, 0x44, 0x0d, 0x0a,  //cora....o..END..
        0x64, 0x27, 0xea, 0x74, 0x72, 0x65, 0x0d, 0x0a, 0x42, 0x45, 0x47, 0x49, 0x4e, 0x0d, 0x0a, 0x72,  //d'être..BEGIN..r
        0xc3, 0xa9, 0x66, 0x6c, 0xc3, 0xa9, 0x63, 0x68, 0x69, 0x72, 0x0d, 0x0a, 0x45, 0x4e, 0x44, 0x0d,  //..fl..chir..END.
        0x0a, 0x66, 0x75, 0x6d, 0xe9, 0x65, 0x0d, 0x0a,                                                  //.fumée..
    };
    L1U8Recode l1U8Recode = L1U8Recode("\r\nBEGIN\r\n", "\r\nEND\r\n");
    size_t outputLen = l1U8Recode.translate(input, sizeof(input), output);
    EXPECT_EQ(sizeof(expectedOutput), outputLen);
    EXPECT_EQ(0, memcmp(output, expectedOutput, outputLen));
}
